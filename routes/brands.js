    doc.font('Helvetica-Bold').text(`RS. ${totalExp.toLocaleString()}`, 250, summaryY + 15, { align: 'right', width: 275 }).fillColor('#059669').text(`RS. ${totalPaid.toLocaleString()}`, 250, summaryY + 35, { align: 'right', width: 275 });
    
