CREATE TABLE IF NOT EXISTS shops (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  status TEXT DEFAULT 'active',
  allowed_panels TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  logo_path TEXT,
  receipt_header_text TEXT,
  receipt_phone TEXT,
  receipt_address TEXT,
  receipt_images_json TEXT,
  receipt_policies TEXT,
  use_logo_on_receipt INTEGER DEFAULT 1,
  header_font_size INTEGER DEFAULT 18,
  header_font_weight TEXT DEFAULT 'bold',
  header_spacing INTEGER DEFAULT 10,
  contact_font_size INTEGER DEFAULT 10,
  contact_align TEXT DEFAULT 'center',
  contact_padding INTEGER DEFAULT 10,
  footer_font_size INTEGER DEFAULT 9,
  footer_font_style TEXT DEFAULT 'normal',
  footer_margin INTEGER DEFAULT 10,
  divider_style TEXT DEFAULT 'dashed',
  divider_width INTEGER DEFAULT 1,
  section_gap INTEGER DEFAULT 10,
  auto_calculate_damage_to_loss INTEGER DEFAULT 1,
  receipt_font_family TEXT DEFAULT 'monospace',
  shop_type TEXT DEFAULT 'retail',
  receipt_extended_name TEXT,
  extended_name_font_size INTEGER DEFAULT 10,
  extended_name_font_weight TEXT DEFAULT 'normal',
  extended_name_spacing INTEGER DEFAULT 2,
  use_text_on_receipt INTEGER DEFAULT 1,
  bill_printer TEXT
);

CREATE TABLE IF NOT EXISTS subscriptions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  shop_id INTEGER NOT NULL REFERENCES shops(id) ON DELETE CASCADE,
  amount DOUBLE PRECISION NOT NULL,
  type TEXT NOT NULL DEFAULT '1_month',
  start_date DATE DEFAULT CURRENT_DATE,
  end_date DATE,
  month TEXT NOT NULL,
  paid_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  shop_id INTEGER REFERENCES shops(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  email TEXT,
  phone TEXT,
  username TEXT NOT NULL UNIQUE,
  password_hash TEXT NOT NULL,
  role TEXT NOT NULL DEFAULT 'user',
  status TEXT DEFAULT 'active',
  allowed_panels TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  panel_permissions TEXT
);

CREATE TABLE IF NOT EXISTS brands (
