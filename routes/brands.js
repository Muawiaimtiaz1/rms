const express = require('express');
const brandService = require('../services/BrandService');
const { requireAuth } = require('../middleware/auth');
const PDFDocument = require('pdfkit');
const router = express.Router();
const db = require('../db/knex');

// GET /api/brands
router.get('/', requireAuth, async (req, res) => {
    let shopId = req.session.user.shop_id;
    if (req.session.user.role === 'superadmin' && req.query.shopId) {
        shopId = parseInt(req.query.shopId, 10);
    }
    const brands = await brandService.listBrands(shopId);
    res.json(brands);
});

// POST /api/brands
router.post('/', requireAuth, async (req, res) => {
    if (req.session.user.role !== 'superadmin') return res.status(403).json({ error: 'Only Master Admins can create brands' });
    const id = await brandService.createBrand(req.body.name, req.body.shopId, req.session.user.id, req.body.ownership_percent, req.body.partner_type);
    res.json({ ok: true, id });
});

// PUT /api/brands/:id
router.put('/:id', requireAuth, async (req, res) => {
    if (req.session.user.role !== 'superadmin') return res.status(403).json({ error: 'Only Master Admins can edit brands' });
    await brandService.updateBrand(req.params.id, req.body.name, req.body.shopId, req.body.ownership_percent, req.body.partner_type);
    res.json({ ok: true });
});

// DELETE /api/brands/:id
router.delete('/:id', requireAuth, async (req, res) => {
    if (req.session.user.role !== 'superadmin') return res.status(403).json({ error: 'Only Master Admins can delete brands' });
    await brandService.deleteBrand(req.params.id, req.query.shopId);
    res.json({ ok: true });
});

// GET /api/brands/expense-shares
router.get('/expense-shares', requireAuth, async (req, res) => {
    const month = req.query.month || new Date().toISOString().slice(0, 7);
    const shares = await brandService.getExpenseShares(req.session.user.shop_id, month);
    res.json(shares);
});

// GET /api/brands/all-months-dues
router.get('/all-months-dues', requireAuth, async (req, res) => {
    const dues = await brandService.getAllMonthsDues(req.session.user.shop_id);
    res.json(dues);
});

// POST /api/brands/expense-payments
router.post('/expense-payments', requireAuth, async (req, res) => {
    await brandService.recordPayment(req.body.brand_id, req.session.user.id, req.body.amount, req.body.month, req.session.user.shop_id);
    res.json({ ok: true });
});

// GET /api/brands/expense-payments
router.get('/expense-payments', requireAuth, async (req, res) => {
    const payments = await brandService.listPayments(req.session.user.shop_id, req.query.month);
    res.json(payments);
});

