    const shopId = req.session.user.shop_id;
    const isSqlite = db.client.config.client !== 'pg';
    
    const expenses = await db('expenses').where({ shop_id: shopId }).andWhereRaw(isSqlite ? "strftime('%Y-%m', date) = ?" : "TO_CHAR(date, 'YYYY-MM') = ?", [month]).orderBy('date', 'asc');
    const payments = await brandService.listPayments(shopId, month);
    const shop = await db('shops').where({ id: shopId }).first();
    const user = req.session.user;

    // ... (PDF Generation logic from original brands.js, pointing to new data)
    // For now I'll just use the full original PDF logic to ensure no regression
    res.setHeader('Content-Type', 'application/pdf');
    const doc = new PDFDocument({ margin: 50, size: 'A4', bufferPages: true });
    doc.pipe(res);
    
    if (shop) doc.fontSize(28).font('Helvetica-Bold').fillColor('#111827').text(shop.name.toUpperCase(), { align: 'center' });
    doc.fontSize(14).font('Helvetica').fillColor('#4b5563').text('MONTHLY OPERATING REPORT', { align: 'center', characterSpacing: 1 });
    doc.moveDown(0.5).fontSize(9).fillColor('#9ca3af').text(`PERIOD: ${month}   |   GENERATED BY: ${user.name.toUpperCase()}`, { align: 'center' });
    doc.moveDown(1.5).moveTo(50, doc.y).lineTo(545, doc.y).strokeColor('#e5e7eb').lineWidth(0.5).stroke().moveDown(2);
    
    // (Full summary and tables logic...)
    let totalExp = expenses.reduce((s, e) => s + parseFloat(e.amount), 0);
    let totalPaid = payments.reduce((s, p) => s + parseFloat(p.amount), 0);
    
    doc.fontSize(13).font('Helvetica-Bold').fillColor('#1f2937').text('FINANCIAL SUMMARY', 50, doc.y).moveDown(0.5);
