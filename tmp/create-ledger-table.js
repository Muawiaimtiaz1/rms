const { getSqlite, getPostgres, usePostgres } = require('./db/runtime');

async function run() {
    console.log('--- Creating saas_financial_logs table ---');
    const isPostgres = usePostgres();
    
    if (isPostgres) {
        const pg = getPostgres();
        try {
            await pg.query(`
                CREATE TABLE IF NOT EXISTS saas_financial_logs (
                    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    shop_id INTEGER REFERENCES shops(id) ON DELETE SET NULL,
                    amount DECIMAL(12,2) NOT NULL,
                    category TEXT NOT NULL,
                    description TEXT,
                    payment_method TEXT DEFAULT 'Cash',
                    created_at TIMESTAMPTZ DEFAULT NOW()
                )
            `);
            console.log('✅ Postgres: saas_financial_logs table created');
            
            // Sync setup fees
            await pg.query(`
                INSERT INTO saas_financial_logs (shop_id, amount, category, description, created_at)
                SELECT id, setup_fee, 'setup', 'Initial Enrollment Fee', created_at 
                FROM shops WHERE setup_fee > 0 
                ON CONFLICT DO NOTHING
            `).catch(e => console.log('Setup sync skipped or failed'));

        } catch (e) {
            console.log('❌ Postgres Error:', e.message);
        }
    } else {
        const db = getSqlite();
        try {
            db.prepare(`
                CREATE TABLE IF NOT EXISTS saas_financial_logs (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    shop_id INTEGER REFERENCES shops(id) ON DELETE SET NULL,
                    amount DECIMAL(12,2) NOT NULL,
                    category TEXT NOT NULL,
                    description TEXT,
                    payment_method TEXT DEFAULT 'Cash',
                    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
                )
            `).run();
            console.log('✅ SQLite: saas_financial_logs table created');
        } catch (e) {
            console.log('❌ SQLite Error:', e.message);
        }
    }
    process.exit(0);
}

run().catch(err => {
    console.error(err);
    process.exit(1);
});
